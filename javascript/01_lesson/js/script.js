// JavaScript Message
console.log("Hello World!");
// Expected Output: Hello World!

// String. A JavaScript string stores a series of characters.
console.log(typeof "James");
// Expected Output: string

// Number. JavaScript number values represent floating-point numbers.
console.log(typeof 42);
// Expected Output: number

// Boolean. JavaScript booleans can have one of two values: true or false.
console.log(typeof true);
// Expected Output: boolean